package common;
/**
 * Record the basic configurations in this simulator
 *
 */
public class Configuration {
	
	/**
	 * number of bits in a word
	 */
	public static final int WORD_LENGTH = 16;
	
	/**
	 * number of words in memory
	 */
	public static final int MEMORY_SIZE = 2048;
	
	/**
	 * number of bits in a GPR(General purpose register)
	 */
	public static final int REG_GPR_LENGTH = 16;
	
	/**
	 * number of bits in index register
	 */
	public static final int REG_IX_LENGTH = 16;
	
	/**
	 * number of bits in condition code
	 */
	public static final int REG_CC_LENGTH = 4;

}
